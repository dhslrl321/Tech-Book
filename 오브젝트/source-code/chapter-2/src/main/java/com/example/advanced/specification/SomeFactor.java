package com.example.advanced.specification;

public class SomeFactor {
    private final int age;
    private final String name;

    public SomeFactor(int age, String name) {
        this.age = age;
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public String getName() {
        return name;
    }
}
